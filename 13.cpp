//calculator

#include <iostream>
using namespace std;

int main()

{
    int a,b;
    char x;

    cout<< " enter the value of a ";
    cin>>a;

    cout<<" enter the value of b ";
    cin>>b;

    cout<<" enter the value of x ";
    cin>>x;

    if 
    (x =='+') 
    {
       cout<< a+b;
    }

    else if 
    (x =='-') 
    {
       cout<< a-b;
    }

    else if 
    (x =='*') 
    {
       cout<< a*b;
    }

    else if 
    (x =='/') 
    {
       cout<< a/b;
    }


    return 0;


}